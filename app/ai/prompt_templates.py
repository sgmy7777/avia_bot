from __future__ import annotations

from app.domain.models import Incident

SYSTEM_PROMPT = """Ты опытный авиационный редактор-копирайтер.
Переписывай только на основе переданных фактов.
Тон: нейтральный, профессиональный, без сенсационности.
Не спекулируй о причинах и не добавляй неподтвержденные данные.
"""


def build_user_prompt(incident: Incident) -> str:
    return f"""
Подготовь пост для Telegram-канала об авиаинциденте.

Требования:
- Объем 150-300 слов.
- Заголовок до 60 символов.
- Формат:
✈️ **Заголовок**

🚨 **Лид: что/когда/где/кто**

⚠️ *Подробности + текущая ситуация + расследование*

- Добавь релевантные хештеги: #авиация #происшествие #[регион].
- Обязательно укажи источник.

Факты:
- Тип события: {incident.event_type}
- Дата: {incident.date_utc}
- Локация: {incident.location}
- Воздушное судно: {incident.aircraft}
- Оператор: {incident.operator or 'нет данных'}
- Людей на борту: {incident.persons_onboard or 'нет данных'}
- Сводка: {incident.summary}
- Источник: {incident.source_url}
""".strip()
